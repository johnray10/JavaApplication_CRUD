    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        if(txt_firstname.getText().isEmpty()){
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane,"FirstName cannot be empty","Warning!",JOptionPane.ERROR_MESSAGE);
            txt_firstname.requestFocus();
        
        }else if(txt_lastname.getText().isEmpty()){
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane,"LastName cannot be empty","Warning!",JOptionPane.ERROR_MESSAGE);
            txt_lastname.requestFocus();
        
        }else if(txt_middlename.getText().isEmpty()){
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane,"MiddleName cannot be empty","Warning!",JOptionPane.ERROR_MESSAGE);
            txt_middlename.requestFocus();
        
        }else if(radio_male.isSelected() == false && radio_female.isSelected() == false){
            getToolkit().beep();
           JOptionPane.showMessageDialog(null, "You must select a valid type to proceed!", "WARNING!", JOptionPane.ERROR_MESSAGE);      
        
        }else if(((JTextField)txt_birthdate.getDateEditor().getUiComponent()).getText().isEmpty()){
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane,"Birthdate cannot be empty","Warning!",JOptionPane.ERROR_MESSAGE);
            txt_birthdate.requestFocus();
        
        }else if(txt_age.getText().isEmpty()){
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane,"Age cannot be empty","Warning!",JOptionPane.ERROR_MESSAGE);
            txt_age.requestFocus();
        
        }else if(txt_address.getText().isEmpty()){
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane,"Address cannot be empty","Warning!",JOptionPane.ERROR_MESSAGE);
            txt_address.requestFocus();
        
        }else if(txt_contact.getText().isEmpty()){
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane,"Contact cannot be empty","Warning!",JOptionPane.ERROR_MESSAGE);
            txt_birthdate.requestFocus();
        
        }else if(imagePath != null){
            
            try {
                String sql = "INSERT INTO `students`(`LastName`, `FirstName`, `MiddleName`, `Gender`, `Birthdate`, `Age`, `Contact`, `Address`, `Picture`,`Status`) "
                        + "VALUES (?,?,?,?,?,?,?,?,?,'Active')";
                pst=conn.prepareStatement(sql);
                pst.setString(1,txt_lastname.getText().toUpperCase());
                pst.setString(2,txt_firstname.getText().toUpperCase());
                pst.setString(3,txt_middlename.getText().toUpperCase());
                radio_male.setActionCommand("Male");
                radio_female.setActionCommand("Female");
                pst.setString(4,buttonGroup1.getSelection().getActionCommand());
                pst.setString(5, ((JTextField)txt_birthdate.getDateEditor().getUiComponent()).getText());
                pst.setString(6,txt_age.getText());
                pst.setString(7,txt_contact.getText().toUpperCase());
                pst.setString(8,txt_address.getText().toUpperCase());
                InputStream img = new FileInputStream(imagePath);
                pst.setBlob(9,img);
                if (pst.executeUpdate() == 1) {
                    JOptionPane.showMessageDialog(this, "Successfully Added!", "ATTENTION", JOptionPane.INFORMATION_MESSAGE);
                    clear();
                    showStudents();
                    Disable();
                    btnClose.setEnabled(true);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,e);
            }
        
        
        //IMAGEVALIDATION
        }else{
            getToolkit().beep();
            JOptionPane.showMessageDialog(this, "No Photo Found!", "ATTENTION", JOptionPane.ERROR_MESSAGE);
            browse();
        }
        
    }//GEN-LAST:event_btnAddActionPerformed
