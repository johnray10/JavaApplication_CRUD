    private void showTableResize(){
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);       
        for (int i = 1; i < 11; i++) {
            
            tblStudentList.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);           
            tblStudentList.getColumnModel().getColumn(i).setPreferredWidth(150);
            
        }

    }
    private void showStudents(){
        try {
            String sql = "SELECT * FROM Students ORDER BY id ASC";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            MainFrame.tblStudentList.setModel(DbUtils.resultSetToTableModel(rs));
            MainFrame.tblStudentList.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            showTableResize();
        } catch (Exception e) {
        }
     }
